<template>
  <Button @click="onClickCreate">Create New Font</Button>
  <Modal
    title="Create New Font"
    :is-open="isOpenModal"
  >
    <template #default>
      <div class="flex flex-col mt-4 gap-2">
        <Input
          v-model="formFields.key"
          label="Unique Identifier Key"
          :disabled="isLoading"
        />
        <Input
          v-model="formFields.name"
          label="Name"
          :disabled="isLoading"
        />
        <Input
          v-model="formFields.font"
          type="file"
          label="Font File"
          accept=".ttf,.woff,.woff2,.otf"
          :disabled="isLoading"
        />
      </div>
    </template>
    <template #bottom-buttons>
      <div class="gap-1 flex">
        <Button
          @click="onClickCloseModal"
          :disabled="isLoading"
          type="secondary"
        >
          Cancel
        </Button>
        <Button
          @click="onClickSubmit"
          :disabled="isLoading"
        >
          Create
        </Button>
      </div>
    </template>
  </Modal>
</template>

<script setup>
import Button from '../../../components/Button/Button.vue';
import Modal from '../../../components/Modal/Modal.vue';
import { ref } from 'vue';
import Input from '../../../components/Input/Input.vue';
import { notify } from '@kyvg/vue3-notification';
import { useLoading } from '../../../composable/useLoading';
import { useRouter } from 'vue-router';
import { fontRepository } from '../../../repositories/font.repository';

const router = useRouter();
const isOpenModal = ref(false);
const { isLoading, startLoading, stopLoading } = useLoading();

const getBlankFields = () => ({
  key: '',
  name: '',
  font: null,
});

const formFields = ref(getBlankFields());

const onClickCreate = () => {
  isOpenModal.value = true;
};

const onClickCloseModal = () => {
  isOpenModal.value = false;
  formFields.value = getBlankFields();
};

const onClickSubmit = async () => {
  startLoading();

  const formData = new FormData();

  Object.entries(formFields.value).forEach(([key, value]) => {
    formData.append(key, value);
  });

  const data = await fontRepository.create(formData);

  stopLoading();

  if (!data) {
    return;
  }

  notify({
    type: 'success',
    title: 'Action OK',
    text: 'Font has been created.',
  });

  onClickCloseModal();
};
</script>

